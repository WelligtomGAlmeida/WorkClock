    public function exchangeArray($data)
    {
        $this->id = (!empty($data['id'])) ? $data['id'] : null;
        $this->razao_social = (!empty($data['razao_social'])) ? $data['razao_social'] : null;
        $this->cnpj = (!empty($data['cnpj'])) ? $data['cnpj'] : null;
